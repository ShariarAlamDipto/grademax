/**
 * Test PDF Builder Module
 */

import { buildWorksheetPDF, questionsToWorksheetItems, savePDF } from './pdf_builder'
import { segmentQuestions } from './segment'
import { parsePDFFromPath } from './parse_pdf_v2'
import type { TextItem } from '../types/ingestion'
import * as path from 'path'

const QP_PATH = 'c:/Users/shari/grademax/data/raw/IGCSE/4PH1/2019/Jun/4PH1_1P.pdf'
const OUTPUT_DIR = 'c:/Users/shari/grademax/output'

async function testPDFBuilder() {
  console.log('ðŸ§ª Testing PDF Builder Module\n')
  
  // Step 1: Parse and segment question paper
  console.log('ðŸ“„ Step 1: Parsing and segmenting question paper...')
  const qpResult = await parsePDFFromPath(QP_PATH)
  const textItems: TextItem[] = []
  for (const page of qpResult.pages) {
    textItems.push(...page.textItems)
  }
  const segmentResult = await segmentQuestions(textItems)
  const questions = segmentResult.questions
  console.log(`  âœ“ Segmented ${questions.length} questions with ${questions.reduce((sum, q) => sum + q.parts.length, 0)} parts\n`)
  
  // Step 2: Select first 3 questions for testing
  console.log('ðŸ“ Step 2: Creating worksheet items...')
  const selectedQuestions = questions.slice(0, 3)
  const items = questionsToWorksheetItems(selectedQuestions, true)
  console.log(`  âœ“ Created ${items.length} worksheet items from ${selectedQuestions.length} questions\n`)
  
  // Step 3: Build worksheet PDF
  console.log('ðŸ”¨ Step 3: Building worksheet PDF...')
  const result = await buildWorksheetPDF({
    sourceQPPath: QP_PATH,
    items,
    config: {
      title: 'Test Worksheet',
      subtitle: 'Questions 1-3',
      includeAnswers: false,
      boxColor: { r: 0, g: 0.5, b: 1 }, // Blue
      boxThickness: 2,
      showLabels: true,
      headerText: 'GradeMax Test Worksheet',
      footerText: 'Generated by GradeMax'
    }
  })
  
  console.log('\nðŸ“Š Build Results:')
  console.log(`  âœ“ Total pages: ${result.metadata.totalPages}`)
  console.log(`  âœ“ Total marks: ${result.metadata.totalMarks}`)
  console.log(`  âœ“ Estimated time: ${result.metadata.estimatedMinutes} minutes`)
  console.log(`  âœ“ Vector regions: ${result.metadata.vectorRegionCount}`)
  console.log(`  âœ“ PDF size: ${Math.round(result.studentPdf.length / 1024)}KB`)
  
  if (result.metadata.layoutWarnings.length > 0) {
    console.log(`  âš ï¸  Warnings: ${result.metadata.layoutWarnings.length}`)
    for (const warning of result.metadata.layoutWarnings) {
      console.log(`    - ${warning}`)
    }
  }
  
  // Step 4: Save PDF
  const outputPath = path.join(OUTPUT_DIR, 'test_worksheet.pdf')
  console.log(`\nðŸ’¾ Step 4: Saving PDF...`)
  savePDF(result.studentPdf, outputPath)
  
  // Validation
  console.log('\nâœ… Validation:\n')
  console.log(`  ${result.metadata.totalPages > 0 ? 'âœ“' : 'âœ—'} PDF has pages`)
  console.log(`  ${result.metadata.vectorRegionCount === items.length ? 'âœ“' : 'âœ—'} All items included`)
  console.log(`  ${result.metadata.layoutWarnings.length === 0 ? 'âœ“' : 'âœ—'} No warnings`)
  console.log(`  ${result.studentPdf.length > 1000 ? 'âœ“' : 'âœ—'} PDF size > 1KB`)
  
  console.log('\nðŸŽ‰ PDF Builder test complete!')
  console.log(`ðŸ“„ Output: ${outputPath}`)
}

testPDFBuilder().catch(console.error)
